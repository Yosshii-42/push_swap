#include "../push_swap.h"

int	is_space(char c)
{
	return ((c >= '\t' && c <= '\r') || c == ' ');
}

int	is_sign(char c)
{
	return (c == '-' || c == '+');
}

int	is_int(long num)
{
	return (num >= -2147483648 && num <= 2147483647);
}
